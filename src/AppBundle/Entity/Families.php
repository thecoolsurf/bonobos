<?php
// src/AppBundle/Entity/Families.php

namespace AppBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Families
 * @ORM\Table(name="families")
 * @ORM\Entity(repositoryClass="AppBundle\Repository\FamiliesRepository")
 */
class Families
{

    /**
     * @var int
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private $id;

    /**
     * @var string
     * @ORM\Column(name="name", type="string", length=50, unique=true)
     */
    private $name;


    /**
     * Get id
     *
     * @return integer
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return Family
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}
